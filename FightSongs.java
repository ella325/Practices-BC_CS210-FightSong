
public class FightSongs {

	public static void main(String[] args) {
		goTeamGo();
		System.out.println();
		repeatM();
		System.out.println();
		repeatM();
		System.out.println();
		goTeamGo();
		
	}
	
	public static void goTeamGo() {
		System.out.println("Go, team, go!");
		System.out.println("You can do it.");
		
	}
	
	public static void youAreTheBest() {
		System.out.println("You're the best,");
		System.out.println("In the west.");
	}
	
	public static void repeatM() {
		goTeamGo();
		youAreTheBest();
		goTeamGo();
		System.out.println();
		
	}
	
	

}
